#include "malloc.h"

/**
 * _calloc - allocate 0'd out memory space
 *
 * @nmemb: number of members in array of memory
 * @size: size of each member of array of memory
 *
 * Return: a pointer to the allocated memory
 */
void *_calloc(size_t nmemb, size_t size)
{
	void *ptr = NULL;
	(void)nmemb;
	(void)size;

	return (ptr);
}
